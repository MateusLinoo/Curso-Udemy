// diferença de Var e Let 1 

var nome = 'Mateus'
var nome = 'Malinouskas' 
console.log(nome)
// a variavel em var será redeclarada, em let será diferente

// podemos também abreviar o var colocando assim:
nome = 'Sophia' // mas não faça isso, irá gerar uma variavel global no sistema

let nome = 'Mateus'
let nome = 'Malinouskas'
// com let não podemos declarar e redeclarar depois pois irá dar um erro